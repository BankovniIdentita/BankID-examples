package cz.bankid.examples.entities.entity;

/**
 * Marital status. One of:
 *
 * {@link COHABITATION} - cohabitation status.
 * {@link MARRIED} - married status
 * {@link DIVORCED} - divorced status
 * {@link REGISTERED_PARTNERSHIP} - registered partnership status
 * {@link REGISTERED_PARTNERSHIP_CANCELED} - registered partnership canceled status
 * {@link WIDOWED} - widowed status
 * {@link SINGLE} - single status
 * {@link UNKNOWN} - unknown status
 */
public enum Maritalstatus {

    COHABITATION,
    MARRIED,
    DIVORCED,
    REGISTERED_PARTNERSHIP,
    REGISTERED_PARTNERSHIP_CANCELED,
    WIDOWED,
    SINGLE,
    UNKNOWN

}
